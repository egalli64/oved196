CREATE TABLE BLUE_TEAMS (
TEAM_ID INT generated BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
TEAM_NAME VARCHAR(30),
PRIMARY KEY(TEAM_ID)
);
CREATE TABLE BLUE_CODERS(
CODER_ID INT generated BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
CODER_FIRSTNAME VARCHAR(30),
CODER_LASTNAME VARCHAR(30),
TEAM_ID INT,
PRIMARY KEY (CODER_ID),
FOREIGN KEY(TEAM_ID) REFERENCES BLUE_TEAMS(TEAM_ID)
);

CREATE TABLE BLUE_ROLES (
ROLE_ID INT generated BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
ROLE_NAME VARCHAR(30)
);

CREATE TABLE ROLES_CODERS (
ROLE_ID INT,
CODER_ID INT,
PRIMARY KEY (ROLE_ID, CODER_ID),
FOREIGN KEY (ROLE_ID) REFERENCES BLUE_ROLES (ROLE_ID),
FOREIGN KEY (CODER_ID) REFERENCES BLUE_CODERS (CODER_ID)
);
